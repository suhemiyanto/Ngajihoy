package com.example.suhemi.gurungaji;

public class SimpanJadwal {
    public String senin;
    public String selasa;
    public String rabu;
    public String kamis;

    public SimpanJadwal() {
    }

    public SimpanJadwal(String senin, String selasa, String rabu, String kamis) {
        this.senin = senin;
        this.selasa = selasa;
        this.rabu = rabu;
        this.kamis = kamis;
    }
}
