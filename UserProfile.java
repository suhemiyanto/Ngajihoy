package com.example.suhemi.gurungaji;

public class UserProfile {
    public String UserNama;
    public String UserJk;
    public String UserNohp;
    public String UserAlamat;
    public String UserEmail;

    public UserProfile() {
    }

    public UserProfile(String userNama, String userJk, String userNohp, String userAlamat, String userEmail) {
        UserNama = userNama;
        UserJk = userJk;
        UserNohp = userNohp;
        UserAlamat = userAlamat;
        UserEmail = userEmail;
    }

}